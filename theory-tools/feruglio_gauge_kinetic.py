def eta_function(q, terms=2000):
    """Dedekind eta: eta(q) = q^(1/24) * prod_{n=1}^inf (1 - q^n)"""
    result = q ** (1.0 / 24)
    for n in range(1, terms + 1):
        qn = q ** n
        if abs(qn) < 1e-16:
            break
        result *= (1 - qn)
    return result


def theta2(q, terms=200):
    """Jacobi theta_2: theta_2(q) = 2*sum_{n>=0} q^{(n+1/2)^2}"""
    s = 0.0
    for n in range(0, terms + 1):
        val = q ** ((n + 0.5) ** 2)
        if val < 1e-16:
            break
        s += 2 * val
    return s


def theta3(q, terms=200):
    """Jacobi theta_3: theta_3(q) = 1 + 2*sum_{n>=1} q^{n^2}"""
    s = 1.0
    for n in range(1, terms + 1):
        val = q ** (n * n)
        if val < 1e-16:
            break
        s += 2 * val
    return s


def theta4(q, terms=200):
    """Jacobi theta_4: theta_4(q) = 1 + 2*sum_{n>=1} (-1)^n * q^{n^2}"""
    s = 1.0
    for n in range(1, terms + 1):
        val = q ** (n * n)
        if val < 1e-16:
            break
        s += 2 * ((-1) ** n) * val
    return s


def sigma_k(n, k):
    """Divisor function sigma_k(n) = sum_{d|n} d^k"""
    s = 0
    for d in range(1, int(n ** 0.5) + 1):
        if n % d == 0:
            s += d ** k
            if d != n // d:
                s += (n // d) ** k
    return s


def E2_func(q, terms=500):
    """Eisenstein series E_2(q) = 1 - 24*sum_{n>=1} sigma_1(n)*q^n"""
    s = 1.0
    for n in range(1, terms + 1):
        sig1 = sigma_k(n, 1)
        contrib = -24 * sig1 * q ** n
        s += contrib
        if abs(contrib) < 1e-15 * max(abs(s), 1):
            return s
    return s


def E4_func(q, terms=200):
    """Eisenstein series E_4(q) = 1 + 240*sum_{n>=1} sigma_3(n)*q^n"""
